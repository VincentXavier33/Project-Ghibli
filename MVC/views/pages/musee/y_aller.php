<h1 class="text-primary bg-secondary">Comment y aller</h1>
<p>
Se rendre physiquement au musée Ghibli n'a rien de bien compliqué. A condition, bien sûr, de se trouver au Japon au préalable... Et plus spécifiquement dans les environs de Tôkyô, c'est encore mieux.
Prenez un train de la ligne JR Chuo ou bien de la ligne Sobu, et descendez à la station Mitaka. Cela vous prendra une trentaine de minutes depuis la gare de Shinjuku, au centre de Tôkyô.



Une fois arrivé à la gare de Mitaka, prenez la sortie sud. Impossible de se tromper, avant de franchir les portillons automatiques, il suffit de lever la tête et elle est indiquée par un panneau jaune « South Exit » qui signale également le musée.

A la sortie de la station, sur votre droite, prenez l'escalator vers les arrêts de bus signalés par un panneau indiquant « Bus Stop ». Une fois en bas, face à vous, se trouve plusieurs arrêts, dont un réservé à la navette spéciale qui dessert directement le musée Ghibli.

La navette spéciale


La navette jaune est aisément identifiable aux dessins « Ghibli » qui l'habille. Elle part toutes les dix minutes.
Les prix des tickets TTC sont les suivants :

Aller ou retour simple	200 ¥
Aller et retour	300 ¥
Enfant de - 12 ans	demi-tarif

Si vous optez pour un aller-retour, plus économique, on vous remettra alors un 1 seul ticket : la partie prédécoupée de droite est à remettre au conducteur pour l'aller, celle de gauche à conserver pour le retour.

A pied


Vous pouvez aussi choisir de marcher le long du canal Tamagawa pour vous rendre au musée, et ainsi découvrir une partie des charmes de la ville de Mitaka que Hayao Miyazaki n'a pas du choisir pour rien.
Pour trouver votre chemin, il suffit une fois encore de regarder autour de vous lorsque vous êtes en bas de l'escalator : de multiples panneaux indiquent la direction. Tous les 200 mètres, un nouveau panneau viendra vous confirmer que vous êtes toujours sur le bon chemin.
Vous pouvez aussi vous reporter au plan ci-dessous pour vous guider.



Cliquez pour agrandir le plan.



Le musée se trouve à environ 1 km, soit environ une quinzaine de minutes de marche à pieds. Lorsque vous apercevrez, derrière un petit portail ouvert, l'accueil du Totoro avec ses premiers visiteurs japonais en train de se faire photographier avec (mais à n'en pas douter, vous ferez de même ^^), félicitation, vous voici arrivé au musée Ghibli !

Par le parc Inokashira


Il existe aussi une petite variante, moins connue. Au lieu de descendre à la station Mitaka, préférez la station précédente, Kichijô-ji.
Le musée Ghibli est situé dans le parc Inokashira Onshi Koen. Celui-ci est splendide, et une fois sur place, une marche très agréable d'une vingtaine de minutes à travers le parc s'offre à vous. Là aussi, pas d'inquiétude, le musée est très bien indiqué par des panneaux.

Quel court métrage allez-vous voir dans la salle de cinéma Saturne ?


Les courts métrages projetés dans la salle de cinéma Saturne alternes périodiquement. Si vous souhaitez savoir à l'avance le titre du film que vous allez voir lors de votre visite, consultez le site officiel du musée Ghibli pour connaître les dernières informations de projection.
Petite astuce pour non-japanophiles : en haut de la page, la partie gauche du tableau vous indique les dates de projection (lecture anglaise) des deux prochains courts, et la partie droite, le nom de ces courts. Cliquez sur le nom pour acceder à la fiche technique du film. A l'aide de la photo alors proposée, reportez-vous à la section de Buta Connnection dédiée aux courts métrages du musée Ghibli pour retrouver son nom et sa fiche française.

Règlement intérieur et recommandations aux visiteurs du musée Ghibli
Une fois à l’intérieur du musée Ghibli, un certain nombre de règles s’appliquent au lieu. Elles vous seront communiquées sur un dépliant, partiellement traduit en français, qui vous sera remis à l’entrée avec votre billet. Nous vous proposons de consulter ces quelques règles, dès à présent, avant votre visite.

Le musée Ghibli est ouvert de 10 h à 18 h. Il est fermé tous les mardis.
Le café « Chapeau de Paille » est ouvert de 11 h à 19 h (dernière commande à 18 h 20).
Les photos ainsi que les vidéos sont interdites, sauf sur la terrasse, le patio, et le café « Chapeau de Paille ».
Chaque visiteur ne peut assister qu'à une seule séance dans la salle de cinéma Saturne.
Il est strictement interdit de fumer dans l'enceinte du musée.
Les téléphones portables doivent être éteints à l'intérieur du musée et ne doivent pas être utilisés là où cela risquerait de déranger les autres visiteurs, y compris à l'extérieur du bâtiment.
La salle d'exposition permanente, au 1er sous-sol, comprend des objets présentés à l'aide de flashes électroniques clignotants qui peuvent être gênants pour les personnes sensibles à la lumière.
La terrasse du musée peut être fermée en cas de mauvais temps.
Le Chat-bus est réservé aux enfants des écoles primaires et moins âgés.
Il est interdit de boire ou de manger dans le musée. Les aliments et boissons apportés par les visiteurs doivent être consommés dans la zone de repos, devant le café, au rez-de-chaussée, et tous les déchets doivent être remportés.
Poussettes et landaus sont à laisser au dépôt prévu à cet effet avant la visite du musée.
Toute introduction d'objets considérés comme dangereux est défendue.
Les animaux ne sont pas admis au musée (sauf chiens d'accompagnement, y compris les chiens d'aveugle).
Tout acte susceptible d'empêcher le bon fonctionnement du musée et de ses installations est défendu.
Les personnes qui risquent de déranger les autres visiteurs d'une manière ou d'une autre ne sont pas admises au musée.
La validité de votre billet prend fin dès votre sortie du musée (pas de réadmission).
Le musée et ses installations sont déchargés de toute responsabilité pour les accidents provoqués par l'inattention des visiteurs.
Le musée ne disposant pas de parking pour les visiteurs, il est recommandé d'éviter de venir en voiture.
Les personnes en fauteuil roulant ont accès à tout le musée (sauf la terrasse), deux ascenseurs et toilettes adaptées étants présents. Le cinéma Saturne possède deux places pour les fauteuils roulants.</p>