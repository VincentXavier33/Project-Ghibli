<h1 class="text-primary bg-secondary">Le Studio de Ghibli</h1>
<p>Introduction
Au Japon, l'animation touche toutes les couches sociales et fait partie intégrante de la culture populaire.
Parmi ses plus grands succès, on retrouve de manière systématique les films d'un « petit » studio, le studio Ghibli, 
fondé par deux des réalisateurs les plus doués de ces 50 dernières années : Hayao Miyazaki et Isao Takahata.

Avec les distributions internationales de Princesse Mononoke, du Voyage de Chihiro, qui a reçu l'Ours d'or au Festival de Berlin,
du Château ambulant, et plus récemment du Vent se lève et du Conte de la princesse Kaguya, les films du studio Ghibli et 
de Miyazaki en particulier sont maintenant connus du grand public en Europe et aux États-Unis. 
Ils ont une importance capitale pour l'image de la production animée nippone qui, en France comme ailleurs, 
a souffert de bien des a priori. En effet, les œuvres du studio sont emblématiques d'une production nationale dont on réalise 
encore à peine, depuis l'étranger, la qualité insoupçonnée et la stupéfiante diversité. Elles constituent, de fait, 
une réfutation exemplaire de toutes les idées préconçues sur le dessin animé japonais.

Nous espérons que le public sera de plus en plus nombreux à découvrir le travail de Hayao Miyazaki et 
de Isao Takahata, dont les réalisations sont, à chaque fois, une promesse renouvelée, un rêve toujours plus beau. 
Car c'est bien ce qui fait le charme de leurs films : ils ne manquent jamais de nous surprendre, de nous émerveiller, 
de nous bouleverser. De nous faire réfléchir aussi... 
L'imaginaire auquel ils font appel et les émotions qu'ils suscitent sont universels. Ainsi, au delà de leur nationalité et 
de leur registre formel, c'est bien dans l'histoire du septième art qu'ils sont de toute évidence appelés à s'inscrire.</p>